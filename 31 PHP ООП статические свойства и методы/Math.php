<?php


/**
 * Class Math
 * класс для математичесикх операций
 */
class Math
{
    /**
     * сложение
     */
    public static function getSum($num1, $num2){
        return $num1 + $num2;
    }

    /**
     * умножение
     */
    public static function getMult($num1, $num2){
        return $num1 * $num2;
    }
}



